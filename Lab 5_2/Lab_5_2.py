#сума перших його трьох цифр дорівнює сумі останніх трьох цифр.
S = 0
for i in range(100,1000,1):
    for k in range(0,1000,1):
       x1 = i // 100;
       x2 = (i // 10) % 10;
       x3 = i % 10;
       y1 = k // 100;
       y2 = (k // 10) % 10;
       y3 = k % 10;
       if (x1+x2+x3) == (y1+y2+y3):
           S = i*1000+k
           print(S)
